import kotlin.math.*

fun main() {
    println(max(5,10)) //Maximo entre 2 valores
    println(min(5,10))//Minimo entre 2 valores
    println(sqrt(45.67f))//Raiz quadrada, valor float
    println(PI)//Usar o PI
    println(E) // Numero de Euler
    println(round(3524.67))//Arredonda o número, espera float ou double, não arredonda inteiro
    
}